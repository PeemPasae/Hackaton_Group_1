#!/bin/bash

LOG_FILE="cart_web.log"
IP="197.82.237.190"

echo "สรุปช่วงเวลาระบบผิดปกติ (รายชั่วโมง)"
echo "จำนวนครั้ง | วันที่และชั่วโมง | HTTP Status"
echo "----------------------------------------"

# 1. หา IP คนร้าย
# 2. กรองเอาเฉพาะบรรทัดที่ error 500(หน่วง) หรือ 504(ล่ม)
# 3. ตัดเอาแค่วันที่และชั่วโมง (13 ตัวอักษรแรก)
# 4. จับกลุ่มนับจำนวน

grep "$IP" "$LOG_FILE" \
    | grep -E '\| 500 \||\| 504 \|' \
    | awk -F'|' '{print substr($1, 1, 13), " | ", $5}' \
    | sort \
    | uniq -c

echo "----------------------------------------"





